    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int n = Integer.parseInt(tam.getText());
        int v[] = new int[n];
        for (int i = 0; i < v.length; i++) {
            v[i]=(int) (Math.random()*10);
        }
        
        for (int i = 0; i < v.length; i++) {
            area.append(" "+v[i]);
        }
        area.append("\n");
        int a = Integer.parseInt(vA.getText());
        int b = Integer.parseInt(vB.getText());
        int aux=0;
        
        aux = v[a];
        v[a] = v[b];
        v[b] = aux;
        for (int i = 0; i < v.length; i++) {
            area.append(" "+v[i]);
        }
        area.append("\n");
    }//GEN-LAST:event_jButton1ActionPerformed

